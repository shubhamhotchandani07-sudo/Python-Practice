#COUNTING FROM 1 TO 50 BY WHILE LOOP

count=1
while count<=50:
    print(count)
    count=count+1

#COUNTING FROM 50 TO 1 BY WHILE LOOP

count=50
while count>=1:
    print(count)
    count=count-1 

#COUNTING FROM -1 TO -50 BY WHILE LOOP

count=-1
while count>=-50:
    print(count)
    count=count-1

#COUNTING FROM -50 TO -1 BY WHILE LOOP

count=-50
while count<=-1:
    print(count)
    count=count+1

